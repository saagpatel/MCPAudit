_BASELINE = """
CREATE SCHEMA app;
CREATE TABLE app.accounts (
  id bigint GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  email text NOT NULL
);
INSERT INTO app.accounts (email) VALUES ('ada@example.test'), ('grace@example.test');
"""

_EXPAND = """
ALTER TABLE app.accounts ADD COLUMN display_name text;
ALTER TABLE app.accounts
  ADD CONSTRAINT accounts_display_name_len
  CHECK (display_name IS NULL OR length(display_name) <= 100) NOT VALID;
"""

_EXPAND_ROLLBACK = """
ALTER TABLE app.accounts DROP CONSTRAINT accounts_display_name_len;
ALTER TABLE app.accounts DROP COLUMN display_name;
"""

_VALIDATE = """
ALTER TABLE app.accounts VALIDATE CONSTRAINT accounts_display_name_len;
"""

_VALIDATE_ROLLBACK = """
ALTER TABLE app.accounts DROP CONSTRAINT accounts_display_name_len;
ALTER TABLE app.accounts
  ADD CONSTRAINT accounts_display_name_len
  CHECK (display_name IS NULL OR length(display_name) <= 100) NOT VALID;
"""

_INDEX = """
CREATE INDEX CONCURRENTLY accounts_email_lower_idx ON app.accounts ((lower(email)));
"""

_INDEX_ROLLBACK = """
DROP INDEX CONCURRENTLY app.accounts_email_lower_idx;
"""


def _phase(
    phase_id: str,
    sql: str,
    rollback_sql: str,
    *,
    mode: str = "transactional",
    effect: str = "schema",
    lock: str = "AccessExclusiveLock",
    expected_object: str = "app.accounts",
    rollback_effect: str | None = None,
    rollback_lock: str | None = None,
    rollback_expected_object: str | None = None,
) -> dict[str, object]:
    return {
        "phase_id": phase_id,
        "mode": mode,
        "sql_digest": digest_bytes(sql.encode()),
        "rollback_type": "compensating_sql" if mode == "nontransactional" else "transaction",
        "rollback_digest": digest_bytes(rollback_sql.encode()),
        "declared_effects": [effect],
        "expected_objects": [expected_object],
        "max_lock_mode": lock,
        "rollback_effects": [rollback_effect or effect],
        "rollback_expected_objects": [rollback_expected_object or expected_object],
        "rollback_max_lock_mode": rollback_lock or lock,
    }


def _manifest(
    phases: list[dict[str, object]] | None = None,
) -> PostgresMigrationManifest:
    if phases is None:
        phases = [
            _phase("expand", _EXPAND, _EXPAND_ROLLBACK),
            _phase(
                "validate",
                _VALIDATE,
                _VALIDATE_ROLLBACK,
                lock="ShareUpdateExclusiveLock",
                rollback_lock="AccessExclusiveLock",
            ),
            _phase(
                "index",
                _INDEX,
                _INDEX_ROLLBACK,
                mode="nontransactional",
                effect="index",
                lock="ShareUpdateExclusiveLock",
                expected_object="app.accounts_email_lower_idx",
                rollback_expected_object="app.accounts_email_lower_idx",
            ),
        ]
    return PostgresMigrationManifest.model_validate(
        {
            "contract": {
                "contract_id": "proofos.postgres-migration",
                "contract_version": "0.1.0",
                "profile": "research-mvp",
            },
            "artifact_id": "accounts-expand-contract",
            "target_postgres_major": 16,
            "baseline_sql_digest": digest_bytes(_BASELINE.encode()),
            "migration_role": "proofos_migrator",
            "fixed_search_path": ["pg_catalog", "app"],
            "observed_tables": [{"schema_name": "app", "table_name": "accounts", "order_by": ["id"]}],
            "lock_timeout_ms": 2_000,
            "statement_timeout_ms": 20_000,
            "phases": phases,
        }
    )


def _manifest_for_baseline(
    baseline: str,
    phases: list[dict[str, object]],
    tables: list[dict[str, object]],
) -> PostgresMigrationManifest:
    payload = _manifest(phases).model_dump(mode="json")
    payload["baseline_sql_digest"] = digest_bytes(baseline.encode())
    payload["observed_tables"] = tables
    return PostgresMigrationManifest.model_validate(payload)


def _artifacts() -> dict[str, PhaseArtifact]:
    return {
        "expand": PhaseArtifact(sql=_EXPAND, rollback_sql=_EXPAND_ROLLBACK),
        "validate": PhaseArtifact(sql=_VALIDATE, rollback_sql=_VALIDATE_ROLLBACK),
        "index": PhaseArtifact(sql=_INDEX, rollback_sql=_INDEX_ROLLBACK),
    }


def _postgres_bindir() -> Path | None:
    configured = os.environ.get("PROOFOS_POSTGRES_BIN")
    candidates = [
        Path(configured) if configured else None,
        Path("/opt/homebrew/opt/postgresql@16/bin"),
        Path("/usr/local/opt/postgresql@16/bin"),
    ]
    pg_config = shutil.which("pg_config")
    if pg_config:
        result = subprocess.run(
            [pg_config, "--bindir"], check=False, capture_output=True, text=True, timeout=5
        )
        if result.returncode == 0:
            candidates.append(Path(result.stdout.strip()))
    for candidate in candidates:
        if candidate and (candidate / "postgres").is_file():
            return candidate
    return None


