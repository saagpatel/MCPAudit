def test_tampered_phase_is_blocked_before_runtime(tmp_path: Path) -> None:
    artifacts = _artifacts()
    artifacts["expand"] = PhaseArtifact(sql=_EXPAND + "\n-- tampered", rollback_sql=_EXPAND_ROLLBACK)
    receipt = verify_postgres_migration(
        _manifest(),
        _BASELINE,
        artifacts,
        binaries=_dummy_binaries(),
        workspace=tmp_path,
    )
    assert receipt.decision == "blocked"
    assert not receipt.source_bound
    assert {finding.code for finding in receipt.findings} == {"PG-SOURCE-SQL-DIGEST"}
    assert not list(tmp_path.iterdir())


