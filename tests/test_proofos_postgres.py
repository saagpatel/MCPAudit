def test_phase_count_bundle_bytes_and_runtime_deadline_are_bounded(tmp_path: Path) -> None:
    base_phase = _phase(
        "phase-0",
        "UPDATE app.accounts SET email = email;",
        "UPDATE app.accounts SET email = email;",
        effect="data",
        lock=LockMode.ROW_EXCLUSIVE.value,
    )
    payload = _manifest([base_phase]).model_dump(mode="json")
    payload["phases"] = [{**base_phase, "phase_id": f"phase-{index}"} for index in range(9)]
    with pytest.raises(ValidationError):
        PostgresMigrationManifest.model_validate(payload)

    padding = "p" * 199_900
    phases: list[dict[str, object]] = []
    artifacts: dict[str, PhaseArtifact] = {}
    for index in range(3):
        sql = f"UPDATE app.accounts SET email = email;\n-- {padding}-{index}"
        rollback = f"UPDATE app.accounts SET email = email;\n-- {padding}-r{index}"
        phase_id = f"bundle-{index}"
        phases.append(
            _phase(
                phase_id,
                sql,
                rollback,
                effect="data",
                lock=LockMode.ROW_EXCLUSIVE.value,
            )
        )
        artifacts[phase_id] = PhaseArtifact(sql=sql, rollback_sql=rollback)
    receipt = verify_postgres_migration(
        _manifest(phases),
        _BASELINE,
        artifacts,
        binaries=_dummy_binaries(),
        workspace=tmp_path,
    )
    assert "PG-SOURCE-BUNDLE-SIZE" in {finding.code for finding in receipt.findings}

    cluster = _DisposablePostgres(_dummy_binaries(), tmp_path, deadline=time.monotonic() - 1)
    with pytest.raises(subprocess.TimeoutExpired):
        cluster._run([Path("/bin/sleep"), "5"], database=None, timeout=30)

    repeated_baseline = _BASELINE + "\n".join(
        f"INSERT INTO app.accounts (email) VALUES ('extra-{index}');" for index in range(24)
    )
    repeated_manifest = _manifest_for_baseline(
        repeated_baseline,
        [_phase("expand", _EXPAND, _EXPAND_ROLLBACK)],
        [{"schema_name": "app", "table_name": "accounts", "order_by": ["id"]}],
    )
    repeated_receipt = verify_postgres_migration(
        repeated_manifest,
        repeated_baseline,
        {"expand": PhaseArtifact(sql=_EXPAND, rollback_sql=_EXPAND_ROLLBACK)},
        binaries=_dummy_binaries(),
        workspace=tmp_path,
    )
    assert "PG-STATIC-BASELINE-TOTAL-ROW-LIMIT" in {finding.code for finding in repeated_receipt.findings}
    assert not list(tmp_path.iterdir())


